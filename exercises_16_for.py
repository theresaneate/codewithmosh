print("\nIterating through String")
for x in "Python":
    print(x)

print("\nIterating through list")
for x in ['a', 'b', 'c']:
    print(x)

print("\nIterating through range to 5")
for x in range(5):
    print(x)

print("\nIterating through range 2 to 5")
for x in range(2, 5):
    print(x)

print("\nIterating through range every 3 in 0 to 10")
for x in range(0, 10, 3):
    print(x)
